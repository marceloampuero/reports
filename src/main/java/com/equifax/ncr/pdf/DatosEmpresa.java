package com.equifax.ncr.pdf;

public class DatosEmpresa {

	private String datosEmpresa;
	private String direccionLaboral;
	private String comunaDireccionLaboral;
	private String ciudadDireccionLaboral;
	private String fechaVerificacionDatos;
	private String fechaActualizacion;
	private String fuenteVerificacion;

	public String getDatosEmpresa() {
		return datosEmpresa;
	}

	public void setDatosEmpresa(String datosEmpresa) {
		this.datosEmpresa = datosEmpresa;
	}

	public String getDireccionLaboral() {
		return direccionLaboral;
	}

	public void setDireccionLaboral(String direccionLaboral) {
		this.direccionLaboral = direccionLaboral;
	}

	public String getComunaDireccionLaboral() {
		return comunaDireccionLaboral;
	}

	public void setComunaDireccionLaboral(String comunaDireccionLaboral) {
		this.comunaDireccionLaboral = comunaDireccionLaboral;
	}

	public String getCiudadDireccionLaboral() {
		return ciudadDireccionLaboral;
	}

	public void setCiudadDireccionLaboral(String ciudadDireccionLaboral) {
		this.ciudadDireccionLaboral = ciudadDireccionLaboral;
	}

	public String getFechaVerificacionDatos() {
		return fechaVerificacionDatos;
	}

	public void setFechaVerificacionDatos(String fechaVerificacionDatos) {
		this.fechaVerificacionDatos = fechaVerificacionDatos;
	}

	public String getFechaActualizacion() {
		return fechaActualizacion;
	}

	public void setFechaActualizacion(String fechaActualizacion) {
		this.fechaActualizacion = fechaActualizacion;
	}

	public String getFuenteVerificacion() {
		return fuenteVerificacion;
	}

	public void setFuenteVerificacion(String fuenteVerificacion) {
		this.fuenteVerificacion = fuenteVerificacion;
	}

}
